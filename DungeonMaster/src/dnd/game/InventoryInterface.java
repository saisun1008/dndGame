package dnd.game;

import dnd.game.entity.item.Chest;
import dnd.game.entity.living.LivingEntity;

/**
 * Represents all entities that contain an inventory. Most commonly the {@link LivingEntity}
 * class, but also the {@link Chest} class.
 */
public interface InventoryInterface {
	/**
	 * Returns the inventory for the entity
	 * @return the inventory of the entity
	 */
	public Inventory getInventory();
}
