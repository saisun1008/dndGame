package dnd.game;


/**
 * An enumeration of player character classes
 * 
 */
public enum PlayerClass {
	FIGHTER("Fighter");
	
	private String name;
	
	/**
	 * Creates a new PlayerClass enum
	 * @param name The human readable name of the class
	 */
	PlayerClass(String name) {
		this.name = name;
	}

	/**
	 * @return The human readable name of this player character class
	 */
	public String getName() {
		return name;
	}
}
